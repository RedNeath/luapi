print([[
Welcome to luapi's setup script!

You are about to follow a guide to help you setup your luapi project!

Disclaimer:
The configuration is going to be applied in real time as you go through
this setup. If your project has already gone through this setup, you'd
better stop this script right now, otherwise some of your configuration
files might be overwritten!!

Press enter when you are ready to start!]])
io.read()


-- 1. Project details
print(" Step 1: project details")
io.write(" License: ")
io.flush()
local license = io.read()
local date = os.date("%Y-%m-%d")


-- 2. Project dependencies
print("\n Step 2: project dependencies")
print([[
 Note: both pegasus and lunajson are required dependenies and will be
 added automatically. You don't need to add them.
]])
local rocks = "pegasus = \"\",\n\tlunajson = \"\""
local rock_file_skeleton = [[
---
--- Autogenerated file, modify it to suit your needs
--- Date: %s
--- License: %s
---
return {
    %s
}
]]

local rock_name = ""
repeat
    io.write(" Rock name (leave blank to stop): ")
    io.flush()
    rock_name = io.read()

    if (rock_name ~= "") then
        io.write(" Rock version (leave blank for latest): ")
        io.flush()
        rocks = string.format("%s,\n\t%s = \"%s\"", rocks, rock_name, io.read())
    end

    print()
until rock_name == ""

local rock_file = io.open("rocks.lua", "w")
rock_file:write(string.format(
    rock_file_skeleton,
    date,
    license,
    rocks
))
rock_file:close()


-- 3. Project environment
print(" Step 3: project environment")
io.write(" Port for the api (default 5100): ")
io.flush()
local port = io.read()
if port == "" then port = "5100" end

local env_variables = string.format("port = \"%s\"", port)
local env_file_skeleton = [[
---
--- Autogenerated file, modify it to suit your needs
--- Date: %s
--- License: %s
---
return {
    %s
}
]]

local env_variable_name = ""
repeat
    io.write(" Variable name (leave blank to stop): ")
    io.flush()
    env_variable_name = io.read()

    if (env_variable_name ~= "") then
        io.write(string.format(" \tvalue: ", env_variable_name))
        io.flush()
        env_variables = string.format("%s,\n\t%s = \"%s\"", env_variables, env_variable_name, io.read())
    end

    print()
until env_variable_name == ""

local env_file = io.open("env.lua", "w")
env_file:write(string.format(
    env_file_skeleton,
    date,
    license,
    env_variables
))
env_file:close()


-- 4. Main script
print(" Step 4: project main script\n")
local main_file_skeleton = [[
---
--- Autogenerated file, modify it to suit your needs
--- Date: %s
--- License: %s
---
local pegasus = require("pegasus")
local lunajson = require("lunajson")
local env = require("env")

local server = pegasus:new({
    port = env.port
})

server:start(function (request, response)
    response:addHeader("Content-Type", "application/json")
    response:write(lunajson.encode({ message = "Your luapi application is ready!" }))
    return response:close()
end)
]]

os.execute("mkdir src -p")
local main_file = io.open("src/main.lua", "w")
main_file:write(string.format(
    main_file_skeleton,
    date,
    license
))
main_file:close()

print("Your project is all set up! You can now execute the start cli script:")
print("> lua bin/cli.lua start")

return 0
